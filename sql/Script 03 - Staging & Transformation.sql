-- Row count validation across all three staging tables
-- Cross-referenced against raw counts to confirm no
-- unintended row loss during transformation
SELECT
    'staging.customers' AS table_name,
    COUNT(*)            AS row_count
FROM staging.customers
UNION ALL
SELECT
    'staging.products',
    COUNT(*)
FROM staging.products
UNION ALL
SELECT
    'staging.activity',
    COUNT(*)
FROM staging.activity;

-- Province standardisation validation
-- Confirms all 26 raw variants have collapsed to
-- 9 clean values with no residual Unknown values
SELECT
    province,
    COUNT(*) AS count
FROM staging.customers
GROUP BY province
ORDER BY count DESC;

-- Gender standardisation validation
SELECT
    gender,
    COUNT(*) AS count
FROM staging.customers
GROUP BY gender
ORDER BY count DESC;

-- Product name standardisation validation
SELECT
    product_name,
    COUNT(*) AS count
FROM staging.products
GROUP BY product_name
ORDER BY count DESC;

-- Duplicate resolution validation
-- Confirms no CustomerID appears more than once
-- in staging.customers
SELECT
    customer_id,
    COUNT(*) AS occurrence_count
FROM staging.customers
GROUP BY customer_id
HAVING COUNT(*) > 1;

-- BIT column validation
-- Confirms is_active_member and churned contain
-- only 0 and 1 values after casting
SELECT
    is_active_member,
    COUNT(*) AS count
FROM staging.activity
GROUP BY is_active_member
ORDER BY is_active_member;

SELECT
    churned,
    COUNT(*) AS count
FROM staging.activity
GROUP BY churned
ORDER BY churned;